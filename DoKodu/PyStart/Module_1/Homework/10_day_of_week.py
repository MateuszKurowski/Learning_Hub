day_number = int(input('Enter the day number of the week: '))

if day_number == 1:
    print('Today is Monday')
elif day_number == 2:
    print('Today is Tuesday')
elif day_number == 3:
    print('Today is Wednesday')
elif day_number == 4:
    print('Today is Thursday')
elif day_number == 5:
    print('Today is Friday')
elif day_number == 6:
    print('Today is Saturday')
elif day_number == 7:
    print('Today is Sunday')
else:
    print('Warning, the week only has 7 days!')
